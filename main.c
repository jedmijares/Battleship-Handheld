int main(void){
	unsigned short xpos = 0;
	short buttons = 0;
  PLL_Init();                           // set system clock to 80 MHz
  Nokia5110_Init();
  SysTick_Init();
  PortF_Init();
  Nokia5110_Clear();
  
	while(1)
	{
		buttons = pushbuttons();
		if(buttons == 0x10)
		{
			xpos--;
			delay1ms(300);
			Nokia5110_Clear();
		}
		else if(buttons == 0x01)
		{
			xpos++;
			delay1ms(300);
			Nokia5110_Clear();
		}
		if(xpos > 84)
			xpos = 80;
		if(xpos < 1)
			xpos = 1;
		Nokia5110_PrintBMP(xpos, 20, Untitled, 0);
		Nokia5110_DisplayBuffer();
	}
}
